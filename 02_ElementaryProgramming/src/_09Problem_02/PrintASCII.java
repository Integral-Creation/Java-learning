package _09Problem_02;

public class PrintASCII {
    public static void main(String[] args) {
        System.out.println(" '0' :"+(int) '0');
        System.out.println(" '1' :"+(int) '1');
        System.out.println(" '2' :"+(int) '2');
        System.out.println(" '3' :"+(int) '3');
        System.out.println(" '4' :"+(int) '4');
        System.out.println(" '5' :"+(int) '5');
        System.out.println(" '6' :"+(int) '6');
        System.out.println(" '7' :"+(int) '7');
        System.out.println(" '8' :"+(int) '8');
        System.out.println(" '9' :"+(int) '9');
        System.out.println(" ' ' :"+(int) ' ');
        System.out.println("\n");
        System.out.println("49:"+(char) 49);
        System.out.println("65:"+(char) 65);
        System.out.println("66:"+(char) 66);
        System.out.println("98:"+(char) 98);
        System.out.println("110:"+(char) 110);
        System.out.println("\n");
        System.out.println((char) 0x4B);
        System.out.println((char) 0x54);
        System.out.println((char) 0x57);
        System.out.println((char) 0xAD);
        System.out.println((char) 0x6c);
        System.out.println('\n');
    }
}
