package _08CharacterDataType;

public class EscapeCharacters {
    public static void main(String[] args) {
        /*
        \b backspace
        \t tab
        \n linefeed         :Moves to the next line.
        \f Formfeed         :Moves to next page (not used)
        \r carriage return  :Moves cursor to beginning of the same line.
        \\ backslash        : print \
        \" double quote     : print "
         */
        System.out.println("Hello\rjava");
        System.out.println("wel\rcome");
    }
}
