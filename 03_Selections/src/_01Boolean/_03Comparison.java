package _01Boolean;

public class _03Comparison {
    public static void main(String[] args) {
        // comparison operator
        System.out.println("'e' < 'f' :"+('e' < 'f'));
        System.out.println("'e' <= 'f' :"+('e' <= 'f'));
        System.out.println("'e' > 'f' :"+('e' > 'f'));
        System.out.println("'e' >= 'f' :"+('e' >= 'f'));
        System.out.println("'e' == 'f'  :"+('e' == 'f'));
        System.out.println("'e' != 'f' :"+('e' != 'f'));
    }
}
